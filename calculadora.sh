n1 = int(input("Digite um número: "))
n2 = int(input("Digite outro número: "))
formula = input ("Digite a formula: ")
if formula == '+':
    print(n1+n2)
elif formula == '-':
    print (n1-n2)

elif formula == '*':
    print (n1*n2)

elif formula == '/':
    print (n1/n2)

else:
    print ("Erro")


